/**
 * @param {string} s
 * @return {number}
 */
var lengthOfLongestSubstring = function (s) {
    const n = s.length;
    let left = 0, max = 0;
    let map = new Map();
    for (let right = 0; right < n; right++) {
        if (map.has(s[right]) && map.get(s[right]) >= left) {
            left = map.get(s[right]) + 1;
        }
        map.set(s[right], right);
        max = Math.max(max, right - left + 1);
    }
    return max;
};
